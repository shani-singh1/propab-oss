def principal_quantum_n(atomic_number: int) -> int:
    """Period proxy for Takahashi-style principal-quantum-number statistics."""
    z = max(1, int(atomic_number))
    if z <= 2:
        return 1
    if z <= 10:
        return 2
    if z <= 18:
        return 3
    if z <= 36:
        return 4
    if z <= 54:
        return 5
    if z <= 86:
        return 6
    return 7


def element_props(symbol: str) -> tuple[int, float, float]:
    sym = (symbol or "").strip().title()
    if sym in _ELEMENT_PROPS:
        return _ELEMENT_PROPS[sym]
    # Fallback: approximate from symbol length / hash — avoids crashes on exotic entries
    z = max(1, len(sym))
    return z, float(z * 2), 1.5
